<div class="section-sub-section automenu">

    <p>The Angels of Death operate five squadrons within War Thunder, comprising upwards of 300 full time
        players and more than 100 part-time players from other AOD divisions.</p>
    <p>We cover all aspects of the game, Arcade, Realistic and Simulator modes in Planes, Tanks, and Naval
        Battles. We’re excited about the future of War Thunder.</p>
    <p>Our members are from around the globe with varying experience levels from beginners to level 100
        veterans. This is a place where people of equal caliber can play together and where newcomers can learn
        and grow. Our forums contain a library of useful information from tank ammunition types, plane
        characteristics, and engagement tactics.</p>

</div>
<div class="section-sub-section automenu">
    <h2 id="servers-and-events">
        Servers and Events
    </h2>

    <p>Angels of Death competes in Realistic Squadron battles and consistently rank in the top 100 tier and
        unite for large Gaijin-organized events.</p>
    <p>Super Squad Saturday is a monthly event hosted by AOD consisting of skill-based competition among our
        community of players, just for fun of course, with a few bragging rights here and there. Classic events
        include bombers versus biplanes, sea-plane racing on water, reserve tanks vs KV2s and air racing. No
        matter what the time we will almost always have people on line and willing to squad up.</p>
    <p>We value good sportsmanship and playing as a team over individual prowess.</p>

