    <h2 id="servers-and-events">
        EVENTS AND OTHER ACTIVITIES
    </h2>

    <p>Angels of Death actively competes in Realistic Squadron Battles. In addition AOD also takes part in various
        Community and Gaijin hosted events like World War Mode, Tournaments and Grind events to name a few.</p>
    <p>Super Squad Saturday is a monthly event hosted by AOD consisting of skill-based competition among our
        community of players, just for fun of course, with a few bragging rights here and there. Classic events
        include bombers versus biplanes, sea-plane racing on water, reserve tanks vs KV2s and air racing. No matter what
        the time is there are always people on line to play, squad up and talk with.</p>
    <p>We value good sportsmanship and playing as a team over individual prowess.</p>

