    <h2 id="events">
        Events
    </h2>

    <h3>Weekly</h3>
    <ul>
        <li><strong>Infantry Training</strong>: Our Reaper team specilizes in cohesive infantry gameplay. Outside of
            Ops, they hold a weekly practice where new players can learn higher level infantry play from the basics to
            the complex, and seasoned players can brush up on their abilities.
        </li>
        <li><strong>Vehicle Training</strong>: Our Guardian Angel team specilizes in coordinated armor/air gameplay.
            Outside of Ops, they hold a weekly practice where members can learn the intracties of working well in
            driver-gunner teams, or holding their own in an armor squad with proper teamwork.
        </li>
        <li><strong>Platoon/Squad Leading Class</strong>: We offer a comprehensive program that can take a complete
            newcomer to leading in-game and guide them through all facets of platoon & squad leading. From how to start
            up a squad, managing a platoon, reading the map, special tactics, and hands on mentorship with real-time
            feedback to help you improve.
        </li>
        <li><strong>Off Faction Night</strong>: To switch things up, we'll spend an evening on NC or VS and get a feel
            for the other faction's arsenals and gameplay. We offer NC and VS outfits for our members to join.
        </li>
        <li><strong>Weekly Operations</strong>: The main event of the week! Every Sunday/Monday, every week, for years,
            we all get together operate multiple platoons in conjunction with our Reaper & Guardian Special Operation
            Groups under a force commander, with officers as platoon and squad leads. AOD conducts Ops with scale and
            coordination that no other outfit can match, and we dominate whatever continent we're on for two hours.
        </li>
    </ul>

    <h3>Special</h3>
    <p>Throughout the month we hold special events for our full-members, always changing and new to keep things fun and
        fresh. From participation in larger community events on live & the Jaeger event server, joint-operations with
        other outfits, private Jaeger minigames, and construction nights on live. There's always something interesting
        coming up.</p>

    <p>Take a look at our <a href="https://planetside.clanaod.net/forums/showthread.php?t=242337">complete schedule of
            events</a>, which includes any upcoming special events.</p>

    <h2>Full Membership</h2>
    <p>Behind every AOD squad and platoon are our full-members. More than just outfit members in-game, these are members
        of AOD as a whole. Full-members have access to our private Forums, Discord, and TeamSpeak where all the action
        happens, as well as the expansive community spanning all our AOD supported games. In-game, full-members also
        enjoy priority spots in platoons, squad/platoon leading, war assets, and thousands of hours of combined
        experience in improvement resources.</p>

