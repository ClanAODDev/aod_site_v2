    <p>Fate has summoned you to the Angels of Death, New World Division.</p>

    <p>This game is set on the fictional and supernatural island of Aeternum in the 17th century. The land of Aeternum
        will be an evolving one that changes with the seasons, weather, and time of day.</p>

    <p>Once you are a member of the New World Division you are welcome to explore a thrilling, open-world MMO filled
        with danger and opportunity where you'll forge a new destiny for yourself alongside friends as you group up and
        explore after being shipwrecked on the supernatural island of Aeternum. There will be endless opportunities to
        fight, and forage among the island's wilderness and ruins. Channel supernatural forces or wield deadly weapons
        in a classless, real-time combat system, and, fight with a small team, or in massed armies for PvE and PvP
        battles—the choices are all yours.</p>

    <p>Opportunities to overcome the brutal legions of The Corrupted and draw battle lines with competing players in
        this land of danger await you. You can work with others to reclaim monster-haunted wilds and build new
        civilizations. In a land hell-bent on your destruction, what will you do to survive? Band together with fellow
        clanmates to forge, fortify, and fight your heroic path, or continue on as a lone wolf?</p>
