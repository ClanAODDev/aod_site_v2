                improve. </p>
            <p>Serious Sunday is our most popular event held every Sunday at 4pm ET in which a competitive scrim is held
                on the Purgatory server. Two captains are selected and pick members to form a team to go head to head in
                a best-of-ten showdown on the selected map.</p>

