    <h2 id="apply-to-division">
        Apply to division
    </h2>

    <p>In order to be eligible for membership in the Elite Dangerous division, you must meet the following
        criteria:</p>
    <ul>
        <li>Be at least 16 years of age</li>
        <li>Be on TeamSpeak whenever you are ingame</li>
        <li>Wear AOD tags while you are ingame</li>
        <li>Contribute to the forums regularly (once biweekly as the bare minimum)</li>
        <li>Be anti-piracy</li>
    </ul>
